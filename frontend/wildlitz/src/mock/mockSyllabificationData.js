  // Helper functions for retrieving words
  export const getRandomWordsByDifficulty = (difficulty, count = 10, excludedWords = [], categories = []) => {
    // Default to easy if an invalid difficulty is provided
    const difficultyLevel = ['easy', 'medium', 'hard'].includes(difficulty) ? difficulty : 'easy';
    
    // Filter by category if specified
    let filteredWords = syllabificationWords[difficultyLevel];
    
    if (categories && categories.length > 0) {
      filteredWords = filteredWords.filter(word => 
        categories.includes(word.category)
      );
    }
    
    // Filter out excluded words
    if (excludedWords && excludedWords.length > 0) {
      const excludedLowerCase = excludedWords.map(w => w.toLowerCase());
      filteredWords = filteredWords.filter(word => 
        !excludedLowerCase.includes(word.word.toLowerCase())
      );
    }
    
    // If we don't have enough words after filtering, just return what we have
    if (filteredWords.length <= count) {
      return filteredWords;
    }
    
    // Otherwise, randomly select the requested number of words
    const shuffled = [...filteredWords].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
  };
  
  export const getWordById = (id) => {
    // Search all difficulty levels for the word with the matching ID
    for (const difficulty of ['easy', 'medium', 'hard']) {
      const word = syllabificationWords[difficulty].find(w => w.id === id);
      if (word) return word;
    }
    return null;
  };
  
  export const getWordByText = (text) => {
    const searchText = text.toLowerCase();
    
    // Search all difficulty levels for the word with matching text
    for (const difficulty of ['easy', 'medium', 'hard']) {
      const word = syllabificationWords[difficulty].find(
        w => w.word.toLowerCase() === searchText
      );
      if (word) return word;
    }
    return null;
  };
  
  // Get all categories present in the data
  export const getAllCategories = () => {
    const categories = new Set();
    
    for (const difficulty of ['easy', 'medium', 'hard']) {
      syllabificationWords[difficulty].forEach(word => {
        categories.add(word.category);
      });
    }
    
    return Array.from(categories);
  };
  
  export default syllabificationWords;